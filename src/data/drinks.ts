    'SHORT_GRASS': '🌿 Трава',
    'POISONOUS_POTATO': '☠️ Ядовитый картофель',
    'BAMBOO': '🎋 Бамбук',
    'OAK_LOG': '🪵 Дубовое бревно',
    'MELON_SLICE': '🍈 Ломтик арбуза',
    'SPIDER_EYE': '🕷️ Паучий глаз',
    'OAK_LEAVES': '🍃 Дубовые листья',
    'SUNFLOWER': '🌻 Подсолнух',
    'BLAZE_POWDER': '🔥 Огненный порошок',
